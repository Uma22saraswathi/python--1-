#convert one type to another type
a = float(1)#int convert to float
print(a)
print(type(a))
b = int(2.5)#float convert to int
print(b)
print(type(b))
c = 3j #int convert to complex
print(c)
print(type(c))
d = 3.0j # float convert to complex
print(d)
print(type(d))
